    private void jButtonConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonConsultarActionPerformed
        // TODO addyo ur handling code here:
       jButtonConsultar.setSelected(false);
        DefaultTableModel val =  (DefaultTableModel) tabelaDados.getModel();
        val.setNumRows(0);
        Artigo artigo = new Artigo();
        ArrayList<Artigo> array = new ArrayList<>();
        try {
            artigo.setID(Integer.parseInt(txtID.getText()));
        } catch (NumberFormatException ex) {
            artigo.setID(0);
        }
        artigo.setEscritor(txtNome.getText());

        array = arrayBD.listaArtigo(artigo);
        if (array != null && array.size() != 0) {
            for (int i = 0; i < array.size(); i++) {
                val.addRow(new String[]{"" + array.get(i).getID(),
                    array.get(i).getEscritor()});
            }
        } else {
            JOptionPane.showMessageDialog(null, "NÃ£o ha nehuma Item cadastrado");
        }
        txtID.setText(null);
        txtNome.setText(null);
    }//GEN-LAST:event_jButtonConsultarActionPerformed

